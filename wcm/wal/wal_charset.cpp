	inline bool _utf8_is_begin_char( unsigned char c )
	{
		return ( !( c & 0x80 ) || ( c & 0xC0 ) == 0xC0 );
	}

	inline int utf8_chars( unsigned c )
	{
		return c < 0x800 ? ( c >= 0x80 ? 2 : 1 ) : ( c >= 0x10000 ? 4 : 3 );
	}


	inline char* to_utf8( char* s, unsigned c )
	{
		if ( c < 0x800 )
		{
			if ( c < 0x80 )
			{
				*( s++ ) = c;
			}
			else
			{
				*( s++ ) = ( 0xC0 | ( c >> 6 ) );
				*( s++ ) = 0x80 | ( c & 0x3F );
			}
		}
		else
		{
			if ( c < 0x10000 ) //1110xxxx 10xxxxxx 10xxxxxx
			{
				s[2] = 0x80 | c & 0x3F;
				c >>= 6;
				s[1] = 0x80 | c & 0x3F;
				c >>= 6;
				s[0] = ( c & 0x0F ) | 0xE0;
				s += 3;
			}
			else     //11110xxx 10xxxxxx 10xxxxxx 10xxxxxx
			{
				s[3] = 0x80 | c & 0x3F;
				c >>= 6;
				s[2] = 0x80 | c & 0x3F;
				c >>= 6;
				s[1] = 0x80 | c & 0x3F;
				c >>= 6;
				s[0] = ( c & 0x7 ) | 0xF0;
				s += 4;
			}
		}

		return s;
	}

	char* unicode_to_utf8( char* s, const unicode_t* buf, int usize, int* badCount )
	{
		if ( usize < 0 )
		{
			for ( ; *buf; buf++ )
			{
				s = to_utf8( s, *buf );
			}
		}
		else
		{
			for ( ; usize > 0; buf++, usize-- )
			{
				s = to_utf8( s, *buf );
			}
		}

		*s = 0;

		if ( badCount ) { *badCount = 0; }

		return s;
	}



	int utf8_string_buffer_len( const unicode_t* buf, int ulen )
	{
		int n = 0;

		if ( ulen >= 0 )
		{
			for ( ; ulen > 0; ulen--, buf++ )
			{
				n += utf8_chars( *buf );
			}
		}
		else
		{
			for ( ; *buf; buf++ )
			{
				n += utf8_chars( *buf );
			}
		}

		return n;
	}

	int utf8_symbol_count( const char* s, int size )
	{
		int cnt = 0;

		if ( size < 0 )
		{
			size = strlen( s );
		}

		int eCount = 0;

		while ( size > 0 )
		{
			if ( *s & 0x80 )
			{
				unicode_t t;
				int n;

				if ( ( *s & 0xE0 ) == 0xC0 )
				{
					t = ( *s & 0x3F );
					n = 1;
				}
				else if ( ( *s & 0xF0 ) == 0xE0 )
				{
					t = ( *s & 0x1F );
					n = 2;
				}
				else if ( ( *s &  0xF8 ) == 0xF0 )
				{
					t = ( *s & 0x07 );
					n = 3;
				}
				else
				{
					eCount++;
					s++;
					cnt++;

					size--;
					continue;
				}

				s++;
				size--;

				for ( ; size > 0 && ( *s & 0xC0 ) == 0x80; size--, n--, s++ )
				{
					t = ( t << 6 ) + ( *s & 0x3F );
				}

				cnt++;

				if ( n != 0 )
				{
					eCount++;
				}

			}
			else
			{
				s++;
				cnt++;

				size--;
			}
		}


		return cnt;
	}



	unicode_t* utf8_to_unicode( unicode_t* buf, const char* s, int size, int* badCount )
	{
		unicode_t* p = buf;

		if ( size < 0 )
		{
			size = strlen( s );
		}

		int eCount = 0;

		while ( size > 0 )
		{
			if ( *s & 0x80 )
			{
				unicode_t t;
				int n;

				if ( ( *s & 0xE0 ) == 0xC0 )
				{
					t = ( *s & 0x3F );
					n = 1;
				}
				else if ( ( *s & 0xF0 ) == 0xE0 )
				{
					t = ( *s & 0x1F );
					n = 2;
				}
				else if ( ( *s &  0xF8 ) == 0xF0 )
				{
					t = ( *s & 0x07 );
					n = 3;
				}
				else
				{
					eCount++;
					*( p++ ) = ( *( s++ ) & 0x7F );
					size--;
					continue;
				}

				s++;
				size--;

				for ( ; size > 0 && ( *s & 0xC0 ) == 0x80; size--, n--, s++ )
				{
					t = ( t << 6 ) + ( *s & 0x3F );
				}

				*( p++ ) = t;

				if ( n != 0 )
				{
					eCount++;
				}

			}
			else
			{
				*( p++ ) = *( s++ );
				size--;
			}
		}

		if ( badCount )
		{
			*badCount = eCount;
		}

		*p = 0;
		return p;
	}

	unicode_t GetChar_UTF8( char* s, char* endLine )
	{
		if ( s >= endLine )
		{
			return 0;
		}

		if ( !( *s & 0x80 ) )
		{
			return *s;
		}

		unicode_t t;
		int n;

		if ( ( *s & 0xE0 ) == 0xC0 )
		{
			t = ( *s & 0x3F );
			n = 1;
		}
		else if ( ( *s & 0xF0 ) == 0xE0 )
		{
			t = ( *s & 0x1F );
			n = 2;
		}
		else if ( ( *s &  0xF8 ) == 0xF0 )
		{
			t = ( *s & 0x07 );
			n = 3;
		}
		else
		{
			return ( unsigned char ) * s;
		}

		s++;
		int size = endLine - s;

		for ( ; size > 0 && ( *s & 0xC0 ) == 0x80; size--, n--, s++ )
		{
			t = ( t << 6 ) + ( *s & 0x3F );
		}

		return t;
	}


	char* GetNext_UTF8( char* s, char* endLine )
	{
		if ( s + 1 >= endLine )
		{
			return 0;
		}

		if ( !( *s & 0x80 ) )
		{
			s++;
		}
		else
		{
			if ( ( *s & 0xE0 ) == 0xC0 )
			{
				s++;

				if ( s >= endLine ) { return 0; }

				if ( ( *s & 0xC0 ) == 0x80 ) { s++; }
			}
			else if ( ( *s & 0xF0 ) == 0xE0 )
			{
				s++;

				if ( s >= endLine ) { return 0; }

				if ( ( *s & 0xC0 ) == 0x80 ) { s++; }

				if ( s >= endLine ) { return 0; }

				if ( ( *s & 0xC0 ) == 0x80 ) { s++; }
			}
			else if ( ( *s &  0xF8 ) == 0xF0 )
			{
				s++;

				if ( s >= endLine ) { return 0; }

				if ( ( *s & 0xC0 ) == 0x80 ) { s++; }

				if ( s >= endLine ) { return 0; }

				if ( ( *s & 0xC0 ) == 0x80 ) { s++; }

				if ( s >= endLine ) { return 0; }

				if ( ( *s & 0xC0 ) == 0x80 ) { s++; }
			}
			else
			{
				s++;
			}
		}

		return s < endLine ? s : 0;
	}


	char* _GetPrev_Utf8( char* s, char* firstLine )
	{
		if ( s <= firstLine ) { return 0; }

		s--;

		if ( ( *s & 0xC0 ) != 0x80 || s <= firstLine ) { return s; } //

		int n = s - firstLine;

		if ( n > 0 )
		{
			if ( ( s[-1] & 0xE0 ) == 0xC0 ||
			     ( s[-1] & 0xF0 ) == 0xE0 ||
			     ( s[-1] &  0xF8 ) == 0xF0 )
			{
				return s - 1;
			}

			if ( ( s[-1] & 0xC0 ) == 0x80 && n > 1 )
			{
				if ( ( s[-2] & 0xF0 ) == 0xE0 || ( s[-2] &  0xF8 ) == 0xF0 ) { return s - 2; }

				if ( ( s[-2] & 0xC0 ) == 0x80 && n > 2 && ( s[-3] &  0xF8 ) == 0xF0 ) { return s - 3; }
			}
		}

		return s;
	}


